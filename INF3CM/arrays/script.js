// criar um array / lista

let lista = ["Pedro","João","Maria","José",33];
console.log(lista);
console.log(lista[3]);

lista[2] = "Paulo";
console.log(lista);

// adicionando elemento na lista , no final 
lista.push("Maria");
console.log(lista);

// remover ultimo elemento
lista.pop();
console.log(lista);

// remove o primeiro elemento
lista.shift()
console.log(lista);