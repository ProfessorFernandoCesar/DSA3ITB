function pedir() {
    let opcao = prompt("Digite a opção");

    switch (opcao) {
        case '1':
            alert("Opção digitada foi 1");
            break;
        case '2':
            alert("Opção digitada foi 2");
            break;
        case '1':
            alert("Opção digitada foi 3");
            break;
        case '1':
            alert("Opção digitada foi 3");
            break;
        case '4':
            alert("Opção digitada foi 4");
            break;
        default:
            alert("digite um valor entre 1 e 4");
            break;
    }





}